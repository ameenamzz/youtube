export function generateRandomMessage() {
  const wordCount = Math.floor(Math.random() * 8) + 3; // 3 to 10 words
  let message = "";

  for (let i = 0; i < wordCount; i++) {
    const randomWord = messageWords[Math.floor(Math.random() * messageWords.length)];
    message += randomWord + " ";
  }

  return message.trim();
}
